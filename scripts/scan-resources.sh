| `AZURE_CLIENT_SECRET` | Entra app secret | env/.env.local | KV: azure-client-secret |
| `AI_FOUNDRY_ENDPOINT` | Azure AI Foundry URL | env/.env.local | App Setting |
| `AI_FOUNDRY_API_KEY` | Azure AI Foundry key | env/.env.local | KV: ai-foundry-api-key |
| `AZURE_OPENAI_ENDPOINT` | OpenAI endpoint | env/.env.local | App Setting |
| `AZURE_OPENAI_API_KEY` | OpenAI API key | env/.env.local | KV: azure-openai-api-key |
| `AZURE_SPEECH_KEY` | Speech service key | env/.env.local | KV: azure-speech-key |
| `AZURE_SPEECH_REGION` | Speech region | env/.env.local | App Setting |
| `AZURE_DALLE_ENDPOINT` | DALL-E endpoint | env/.env.local | App Setting |
| `AZURE_DALLE_API_KEY` | DALL-E key | env/.env.local | KV: azure-dalle-api-key |
| `AZURE_SEARCH_ENDPOINT` | AI Search endpoint | env/.env.local | App Setting |
| `AZURE_SEARCH_ADMIN_KEY` | AI Search admin key | env/.env.local | KV: azure-search-admin-key |
| `DATABASE_URL` | Database connection string | SQLite (auto) | KV: database-url |
| `JWT_SECRET_KEY` | JWT signing key | auto-generated (dev) | KV: jwt-secret-key |
| `APPLICATIONINSIGHTS_CONNECTION_STRING` | App Insights | optional | App Setting (Bicep) |
| `AZURE_KEY_VAULT_NAME` | Key Vault name | optional | App Setting (Bicep) |
| `NEXT_PUBLIC_API_URL` | Backend URL for frontend | http://localhost:8000 | App Setting (Bicep) |
| `NEXT_PUBLIC_AZURE_AD_CLIENT_ID` | MSAL client ID | env/.env.local (frontend) | App Setting (frontend) |
| `NEXT_PUBLIC_AZURE_AD_TENANT_ID` | MSAL tenant ID | env/.env.local (frontend) | App Setting (frontend) |

## External Dependencies (Not Auto-Provisioned)

These must be manually provisioned or provided:

- [ ] Azure AI Foundry deployment (models: gpt-4.1, Kimi-K2.5, Mistral-Large-3, text-embedding-3-small, dall-e-3)
- [ ] Azure AI Search index (optional; needed for enterprise RAG)
- [ ] Azure SQL Database (optional; SQLite used for local dev)
- [ ] Azure Cognitive Speech resource
- [ ] Azure Document Intelligence resource
- [ ] Entra app registration (Meeting-Intelligence-Bot) — tenant: 588cadf4-9902-4465-86c0-8bcf04f4f102

## Key Vault Secrets Checklist

Run this after Bicep provisioning to populate secrets:

```bash
KV=kv-armely-ai
az keyvault secret set --vault-name $KV --name jwt-secret-key         --value "$(python3 -c 'import secrets; print(secrets.token_hex(32))')"
az keyvault secret set --vault-name $KV --name azure-client-secret    --value "$AZURE_CLIENT_SECRET"
az keyvault secret set --vault-name $KV --name ai-foundry-api-key     --value "$AI_FOUNDRY_API_KEY"
az keyvault secret set --vault-name $KV --name azure-openai-api-key   --value "$AZURE_OPENAI_API_KEY"
az keyvault secret set --vault-name $KV --name azure-speech-key       --value "$AZURE_SPEECH_KEY"
az keyvault secret set --vault-name $KV --name azure-dalle-api-key    --value "$AZURE_DALLE_API_KEY"
az keyvault secret set --vault-name $KV --name azure-search-admin-key --value "$AZURE_SEARCH_ADMIN_KEY"
az keyvault secret set --vault-name $KV --name database-url           --value "$DATABASE_URL"
```
FOOTER

echo "Written: $OUTPUT_MD"

# ── Write JSON inventory ───────────────────────────────────────────────────────
cat > "$OUTPUT_JSON" << JSON
{
  "generatedAt": "$(date -u +%Y-%m-%dT%H:%M:%SZ)",
  "services": {
    "azureOpenAI":          $NEEDS_OPENAI,
    "azureSpeech":          $NEEDS_SPEECH,
    "azureDalle":           $NEEDS_DALLE,
    "azureSearch":          $NEEDS_SEARCH,
    "azureStorage":         $NEEDS_STORAGE,
    "azureSql":             $NEEDS_SQL,
    "azureCosmos":          $NEEDS_COSMOS,
    "redis":                $NEEDS_REDIS,
    "azureTranslator":      $NEEDS_TRANSLATOR,
    "documentIntelligence": $NEEDS_DOC_INTELLIGENCE,
    "appInsights":          $NEEDS_APPINSIGHTS,
    "keyVault":             $NEEDS_KEYVAULT
  }
}
JSON

echo "Written: $OUTPUT_JSON"
echo "Done."
