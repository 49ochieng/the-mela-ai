#!/usr/bin/env bash
# =============================================================================
# scripts/scan-resources.sh — Mela AI Resource Scanner
# Scans the repository for environment variable references to determine
# which Azure services are required. Outputs docs/resources-required.md
# and scripts/resource-inventory.json.
# =============================================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
DOCS_DIR="$REPO_ROOT/docs"
OUTPUT_MD="$DOCS_DIR/resources-required.md"
OUTPUT_JSON="$SCRIPT_DIR/resource-inventory.json"

mkdir -p "$DOCS_DIR"

echo "Scanning repository for Azure resource requirements..."

# ── Helper: grep for a key in the repo ────────────────────────────────────────
key_used() {
  local key="$1"
  grep -rq "$key" "$REPO_ROOT/backend" "$REPO_ROOT/frontend" \
       --include="*.py" --include="*.ts" --include="*.tsx" --include="*.js" \
       --include="*.env*" --include="*.sample" 2>/dev/null
}

# ── Detect required services ───────────────────────────────────────────────────
NEEDS_OPENAI=false
NEEDS_SPEECH=false
NEEDS_DALLE=false
NEEDS_SEARCH=false
NEEDS_STORAGE=false
NEEDS_SQL=false
NEEDS_COSMOS=false
NEEDS_REDIS=false
NEEDS_TRANSLATOR=false
NEEDS_DOC_INTELLIGENCE=false
NEEDS_APPINSIGHTS=false
NEEDS_KEYVAULT=false

key_used "AI_FOUNDRY_ENDPOINT\|AZURE_OPENAI_ENDPOINT\|AZURE_OPENAI_API_KEY"    && NEEDS_OPENAI=true
key_used "AZURE_SPEECH_KEY\|AZURE_SPEECH_ENDPOINT"                             && NEEDS_SPEECH=true
key_used "AZURE_DALLE"                                                          && NEEDS_DALLE=true
key_used "AZURE_SEARCH_ENDPOINT\|AZURE_SEARCH_API_KEY\|AZURE_SEARCH_ADMIN_KEY" && NEEDS_SEARCH=true
key_used "AZURE_STORAGE_ACCOUNT\|AZURE_STORAGE_CONNECTION\|BLOB_CONTAINER"     && NEEDS_STORAGE=true
key_used "AZURE_SQL_SERVER\|DATABASE_URL"                                       && NEEDS_SQL=true
key_used "AZURE_COSMOS_ENDPOINT\|COSMOS_ENDPOINT"                               && NEEDS_COSMOS=true
key_used "REDIS_URL\|AZURE_REDIS"                                               && NEEDS_REDIS=true
key_used "AZURE_TRANSLATOR_KEY\|AZURE_TRANSLATOR_ENDPOINT"                      && NEEDS_TRANSLATOR=true
key_used "AZURE_DOCUMENT_INTELLIGENCE"                                          && NEEDS_DOC_INTELLIGENCE=true
key_used "APPLICATIONINSIGHTS_CONNECTION_STRING\|APP_INSIGHTS"                  && NEEDS_APPINSIGHTS=true
key_used "AZURE_KEY_VAULT\|KEY_VAULT"                                           && NEEDS_KEYVAULT=true

# ── Write Markdown report ──────────────────────────────────────────────────────
cat > "$OUTPUT_MD" << 'HEADER'
# Mela AI — Azure Resources Required

> Auto-generated by `scripts/scan-resources.sh`. Re-run after adding new integrations.

## Architecture Overview

This application deploys as two Azure App Service instances (Linux):
- **armely-ai-api** — FastAPI backend (Python 3.12)
- **armely-ai-web** — Next.js 14 frontend (Node.js 20 LTS)

Both share a single App Service Plan (`asp-armely-ai`) and a Key Vault (`kv-armely-ai`).

HEADER

echo "" >> "$OUTPUT_MD"
echo "## Required Resources Detected in Codebase" >> "$OUTPUT_MD"
echo "" >> "$OUTPUT_MD"
echo "| Resource | Required Local | Required Prod | Where Configured |" >> "$OUTPUT_MD"
echo "|----------|:---:|:---:|---|" >> "$OUTPUT_MD"
echo "| App Service Plan (asp-armely-ai) | — | ✓ | Bicep auto-provisioned |" >> "$OUTPUT_MD"
echo "| Backend Web App (armely-ai-api) | — | ✓ | Bicep auto-provisioned |" >> "$OUTPUT_MD"
echo "| Frontend Web App (armely-ai-web) | — | ✓ | Bicep auto-provisioned |" >> "$OUTPUT_MD"
echo "| Key Vault (kv-armely-ai) | — | ✓ | Bicep auto-provisioned |" >> "$OUTPUT_MD"
echo "| App Insights (ai-armely-ai) | ✗ | ✓ | Bicep auto-provisioned |" >> "$OUTPUT_MD"

[ "$NEEDS_SQL" = true ]              && echo "| Azure SQL / DATABASE_URL | SQLite | ✓ | env/.env.local → KV secret: database-url |" >> "$OUTPUT_MD"
[ "$NEEDS_OPENAI" = true ]           && echo "| Azure OpenAI / AI Foundry | ✓ | ✓ | env/.env.local → KV secret: ai-foundry-api-key |" >> "$OUTPUT_MD"
[ "$NEEDS_SPEECH" = true ]           && echo "| Azure Cognitive Speech | ✗ | ✓ | env/.env.local → KV secret: azure-speech-key |" >> "$OUTPUT_MD"
[ "$NEEDS_DALLE" = true ]            && echo "| Azure DALL-E (Image Generation) | ✗ | ✓ | env/.env.local → KV secret: azure-dalle-api-key |" >> "$OUTPUT_MD"
[ "$NEEDS_SEARCH" = true ]           && echo "| Azure AI Search | ✗ | ✓ | env/.env.local → KV secret: azure-search-admin-key |" >> "$OUTPUT_MD"
[ "$NEEDS_STORAGE" = true ]          && echo "| Azure Blob Storage | ✗ | ✓ | env/.env.local → KV secret: azure-storage-account-key |" >> "$OUTPUT_MD"
[ "$NEEDS_COSMOS" = true ]           && echo "| Azure Cosmos DB | ✗ | Optional | env/.env.local |" >> "$OUTPUT_MD"
[ "$NEEDS_REDIS" = true ]            && echo "| Azure Cache for Redis | ✗ | Optional | env/.env.local |" >> "$OUTPUT_MD"
[ "$NEEDS_TRANSLATOR" = true ]       && echo "| Azure Translator | ✗ | ✓ | env/.env.local |" >> "$OUTPUT_MD"
[ "$NEEDS_DOC_INTELLIGENCE" = true ] && echo "| Azure Document Intelligence | ✗ | ✓ | env/.env.local |" >> "$OUTPUT_MD"

echo "" >> "$OUTPUT_MD"

cat >> "$OUTPUT_MD" << 'FOOTER'
## Environment Variable Mapping

